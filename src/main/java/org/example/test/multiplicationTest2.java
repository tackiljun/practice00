package org.example.test;

public class multiplicationTest2 {

    public static void main(String[] args) {
        
        System.out.println("==========구구단 심화2==========");

        // 구구단 심화2.
        for(int i = 1; i < 10; i++) {

            int result = (9 * i) + (8 * i);

            System.out.println("(" + "9 X " + i + ")" + " + " + "(" + "8 X " + i + ")" + " = " + result);

        }

        System.out.println("==========구구단 심화2==========");

    }
    
}
